// Package constellation implements Nancy Roman's algorithm
// for determining the constellation containing an astronomical coordinate pair,
// as detailed in "Identification of a Constellation from a Position" (1987).
package constellation

import "errors"

// At returns the name of the constellation containing
// the given right ascension (in hours) and declination (in degrees)
// in the B1875.0 epoch.
//
// At returns an error if ra is outside the range [0, 24)
// or dec is outside the range [-90, 90].
func At(ra, dec float64) (string, error) {
	if dec < -90 || dec > 90 {
		return "", errors.New("invalid declination")
	}
	for _, b := range bounds {
		if b.dec <= dec && b.ral <= ra && ra < b.rau {
			return b.con, nil
		}
	}
	return "", errors.New("invalid right ascension")
}

